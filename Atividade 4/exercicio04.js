// 4. Faça um algoritmo que peça ao usuário 4 valores inteiros e mostre-os em ordem decrescente. 

function ordemDecrescente(){
    const numeros = [];
    num1 = parseInt(prompt("Digite um número inteiro: "));
    numeros[0] = num1;
    num2 = parseInt(prompt("Digite mais número inteiro: "));
    numeros[1] = num2;
    num3 = parseInt(prompt("Digite mais número inteiro: "));
    numeros[2] = num3;
    num4 = parseInt(prompt("Digite mais número inteiro: "));
    numeros[3] = num4;
 
}
